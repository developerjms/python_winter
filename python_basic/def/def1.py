# 사칙연산 프로그램 단일 연산식 입력해서 연산 결과 출력

expr = input("수식입력 : ")

sepExpr = expr.split(' ')

print(sepExpr)

if sepExpr [1] == '+':
    print(int(sepExpr[0]) + int(sepExpr[2]))

elif sepExpr [1] == '-':
    print(int(sepExpr[0]) + int(sepExpr[2]))

elif sepExpr [1] == '*':
    print(int(sepExpr[0]) + int(sepExpr[2]))

elif sepExpr [1] == '/':
    print(int(sepExpr[0]) + int(sepExpr[2]))